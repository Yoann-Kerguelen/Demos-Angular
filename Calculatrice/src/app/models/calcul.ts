export class Calcul {

    constructor() {
    }

    addition(a: number, b: number) {
        return a + b;
    }

    soustraction(a: number, b: number) {
        return a - b;
    }

    multiplication(a: number, b: number) {
        return a * b;
    }

    division(a: number, b: number) {
        return a / b;
    }

    modulo(a: number, b: number) {
        return a % b;
    }
}
